    private void bAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAceptarActionPerformed
        // TODO add your handling code here:
        if (tfNombre.getText().isEmpty() || tfApellido.getText().isEmpty() || tfDni.getText().isEmpty()) {
           lError.setVisible(true);
        }else{
            lError.setVisible(false);
            validarNombre(tfNombre.getText());
            validarApellido(tfApellido.getText()); 
            validarDni(tfDni.getText());
            if (validarNombre(tfNombre.getText()) == true && validarApellido(tfApellido.getText()) == true && validarDni(tfDni.getText()) ==true) {
               Ej2.altaPersonas(tfNombre.getText(),tfApellido.getText(),tfDni.getText());
               vaciarCampos(); 
               lNombreInvalido.setVisible(false);
               lApellidoInvalido.setVisible(false);
               lDniInvalido.setVisible(false);
            }
            
            
        }
    }//GEN-LAST:event_bAceptarActionPerformed

