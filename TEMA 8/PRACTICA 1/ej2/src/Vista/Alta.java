    private void vaciarCampos() {
        tfNombre.setText("");
        tfApellido.setText("");
        tfDni.setText("");
    }


    private Boolean validarNombre(String nombre) {
        Pattern pat = Pattern.compile("^[A-Z][a-z]{2,}$");
        Matcher mat = pat.matcher(nombre);
        if (mat.matches()) {
            return true;
        }else
            lNombreInvalido.setVisible(true);
            return false;
    }

    private Boolean validarApellido(String apellido) {
        Pattern pat = Pattern.compile("^[A-Z][a-z]{2,}$");    
        Matcher mat = pat.matcher(apellido);
        if (mat.matches()) {
            return true;
        }else
            lApellidoInvalido.setVisible(true);
            return false;
        
    }

    private Boolean validarDni(String dni) {
        Pattern pat = Pattern.compile("^[0-9]{8}[ABCDEFGHJKLMNPQRSTWXYZ]$");    
        Matcher mat = pat.matcher(dni);
        if (mat.matches()) {
            return true;
        }else
            lDniInvalido.setVisible(true);
            return false;   
    }
