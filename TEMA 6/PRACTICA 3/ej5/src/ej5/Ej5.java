package ej5;

import java.util.regex.Matcher;
import java.util.regex.Pattern;
import javax.swing.JOptionPane;

/**
 *
 * @author 1gdaw06
 */
public class Ej5 {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        // TODO code application logic here
        String ip = JOptionPane.showInputDialog("Introduce una IP");
        Pattern pat = Pattern.compile("^(([0-9]|[1-9][0-9]|1[0-9]{2}|2[0-4][0-9]|25[0-5])\\.){3}([0-9]|[1-9][0-9]|1[0-9]{2}|2[0-4][0-9]|25[0-5])$");
        Matcher mat = pat.matcher(ip);
        if (mat.matches()) {
            JOptionPane.showMessageDialog(null, "la ip es correcta");
        }else
            JOptionPane.showMessageDialog(null, "la ip no es correcta");
    }
    
}
